package software.amazon.dsql.rewards.model;

import java.util.UUID;

public class OrderItem {
    private UUID catalogItemId;
    private String name;
    private String description;
    private long pointsPrice;
    private int quantity;

    public UUID getCatalogItemId() {
        return catalogItemId;
    }

    public void setCatalogItemId(UUID catalogItemId) {
        this.catalogItemId = catalogItemId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public long getPointsPrice() {
        return pointsPrice;
    }

    public void setPointsPrice(long pointsPrice) {
        this.pointsPrice = pointsPrice;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public long getPointsTotal() {
        return pointsPrice * quantity;
    }
}
